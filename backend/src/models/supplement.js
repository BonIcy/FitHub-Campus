let mongoose = require('mongoose');

let supplementSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

let Supplement = mongoose.model('Supplement', supplementSchema);

module.exports = Supplement;
